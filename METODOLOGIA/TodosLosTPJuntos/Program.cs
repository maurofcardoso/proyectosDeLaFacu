		public static void llenar (IColeccionable x) {
			for (int y = 0; y < 2; y++) {
				IComparable numeroAuxiliar = new CNumero (10);
				x.agregar (numeroAuxiliar);
			}
		}
		public static void informar (IColeccionable x) {
			Console.WriteLine ("EL COLECCIONABLE TIENE {0} ELEMENTOS. ", x.cuantos () );
			Console.WriteLine ("EL MINIMO DEL COLECCIONABLE ES: {0}", x.minimo () );
			Console.WriteLine ("EL MAXIMO DEL COLECCIONABLE ES: {0}", x.maximo () );
			Console.WriteLine ("INGRESE EL NOMBRE: ");
			string nombre = Console.ReadLine ();
			Console.WriteLine ("INGRESE EL APELLIDO: ");
			string apellido = Console.ReadLine ();
			Console.WriteLine ("INGRESE EL DNI: ");
			CNumero dni = new CNumero (int.Parse (Console.ReadLine () ) );
			IComparable numeroAuxiliar = new CAlumno (nombre, apellido, dni, new CNumero (505358), new CNumero (8) );
			if (x.contiene (numeroAuxiliar) ) {
				Console.WriteLine ("EL ELEMENTO LEIDO ESTA EN LA COLECCION!!");
			}
			else {
				Console.WriteLine ("EL ELEMENTO LEIDO NO ESTA EN LA COLECCION!!");
			}
		}
