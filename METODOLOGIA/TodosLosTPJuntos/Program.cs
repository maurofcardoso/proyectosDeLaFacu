		public static void jornadaDeVentas (IColeccionable x) {
			for (int y = 0; y < 20; y++) {
				CGeneradorDeDatosAleatorios auxiliarAleatorio = new CGeneradorDeDatosAleatorios ();
				IIterador auxiliar = x.crearIterador ();
				while (!auxiliar.fin () ) {
					CNumero monto = (CNumero) auxiliarAleatorio.numeroAleatorio (7000);
					CVendedor vendedorAuxiliar = (CVendedor) auxiliar.actual ();
					vendedorAuxiliar.setMonto (monto.getValor () );
					vendedorAuxiliar.venta (monto.getValor () );
					auxiliar.siguiente ();
				}
			}
		}
		public static CPersona empezarJuego (CPersona x, CPersona y) {
			CJuegoCartas jugar = new CJuegoTruco ();
			int ganador = jugar.empezarJuego ();
			if (ganador == 1) {
				return x;
			}
			else {
				return y;
			}
		}
