		public static void llenarAlumnos (IColeccionable x) {
			for (int y = 0; y < 2; y++) {
				CAlumno alumnoAuxiliar = new CAlumno ("MAURO", "CARDOSO", new CNumero (31282983), new CNumero (505358), new CNumero (8) );
				alumnoAuxiliar.setComparar ("NOMBRE");
				x.agregar (alumnoAuxiliar);
			}
		}
		public static void imprimirElementos (IColeccionable x) {
			IIterador auxiliar = x.crearIterador ();
			while (!auxiliar.fin () ) {
				Console.WriteLine (auxiliar.actual () );
				auxiliar.siguiente ();
			}
		}
		public static void cambiarEstrategia (IColeccionable x, string estrategia) {
			IIterador auxiliar = x.crearIterador ();
			while (!auxiliar.fin () ) {
				auxiliar.actual ().setComparar (estrategia);
				auxiliar.siguiente ();
			}
		}
		public static void llenarTP3 (IColeccionable x, string opcion) {
			for (int q = 0; q < 2;q++) {
